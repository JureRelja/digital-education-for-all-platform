export function RIASECTypeEn(letter: string) {
  if (letter == "R") {
    return "Realistic (Doers)";
  } else if (letter == "I") {
    return "Investigative (Thinkers)";
  } else if (letter == "A") {
    return "Artistic (Creators)";
  } else if (letter == "S") {
    return "Social (Helpers)";
  } else if (letter == "E") {
    return "Enterprising (Persuaders)";
  } else if (letter == "C") {
    return "Conventional (Organizers)";
  }
}
